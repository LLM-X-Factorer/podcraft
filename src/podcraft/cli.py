@main.command()
@click.option("--language", "-l", default="en", help="Podcast language (en, zh, ja)")
@click.option("--title", "-t", default="My Podcast", help="Podcast title")
def init(language: str, title: str):
    """Initialize a new PodCraft project in the current directory."""
    config_path = Path.cwd() / CONFIG_FILENAME
    if config_path.exists():
        click.echo(f"  {CONFIG_FILENAME} already exists. Aborting.")
        return

    lang_short = language[:2]
    voices = DEFAULT_VOICES.get(lang_short, DEFAULT_VOICES["en"])

    if lang_short == "zh":
        host_name, guest_name = "小刘", "美美"
        desc = f"由 PodCraft 生成的播客。"
    elif lang_short == "ja":
        host_name, guest_name = "タロウ", "ハナコ"
        desc = f"PodCraftで生成されたポッドキャスト。"
    else:
        host_name, guest_name = "Alex", "Sam"
        desc = f"A podcast generated by PodCraft."

    config_content = f"""podcast:
  title: "{title}"
  description: "{desc}"
  author: ""
  language: "{language}"
  category: "Technology"
  link: ""
  cover_url: ""

hosts:
  host:
    name: "{host_name}"
    voice: "{voices['host']}"
  guest:
    name: "{guest_name}"
    voice: "{voices['guest']}"

llm:
  engine: "gemini"       # gemini, anthropic, openai, auto
  model: ""              # empty = default per engine
  temperature: 0.7

tts:
  engine: "edge"         # edge (free), volcano_podcast (paid, high quality)
  silence_duration: 1.5
  sample_rate: 44100
  bitrate: "192k"

cover:
  engine: "disabled"     # disabled | placeholder (no API) | imagen (Gemini Imagen 4)
  size: 1400
  overlay:
    title: "{title}"
    subtitle: ""
    title_font_size: 80
    subtitle_font_size: 28
    title_color: [212, 175, 105]
    subtitle_color: [180, 155, 100]
  # theme_keywords:      # optional: map keywords in episode title to custom Imagen prompts
  #   "music": "Abstract sound waves and musical notes, artistic, no text"
  #   "tech": "Futuristic circuit patterns, blue tones, no text"

shownotes:
  enabled: true
  max_tokens: 1500
  temperature: 0.5

release:
  enabled: false         # set to true to upload audio to GitHub Releases
  repo: ""               # "owner/repo" — auto-detected from git remote if empty
  tag: "v1.0.0-podcast"

feed:
  audio_base_url: ""     # URL where your MP3s are hosted
  output: "podcast-feed.xml"

paths:
  output: "output/"
  scripts: "scripts/"
  prompts: "prompts/"
"""
    config_path.write_text(config_content, encoding="utf-8")

    # Create directories
    for d in ["output", "scripts", "prompts", "episodes"]:
        (Path.cwd() / d).mkdir(exist_ok=True)

    # Copy language-appropriate prompt templates
    src_template = TEMPLATES_DIR / f"system_{lang_short}.md"
    if not src_template.exists():
        src_template = TEMPLATES_DIR / "system_en.md"
    shutil.copy2(src_template, Path.cwd() / "prompts" / "system.md")

    src_notes = TEMPLATES_DIR / f"shownotes_{lang_short}.md"
    if not src_notes.exists():
        src_notes = TEMPLATES_DIR / "shownotes_en.md"
    if src_notes.exists():
        shutil.copy2(src_notes, Path.cwd() / "prompts" / "shownotes.md")

    click.echo(f"  Initialized PodCraft project ({language})")
    click.echo(f"  Created: {CONFIG_FILENAME}, prompts/system.md, prompts/shownotes.md")
    click.echo(f"\n  Next steps:")
    click.echo(f"  1. Export your LLM API key: export GEMINI_API_KEY=...")
    click.echo(f"  2. Put a Markdown file in episodes/")
    click.echo(f"  3. Run: podcraft publish episodes/your-doc.md")


