@main.command()
@click.argument("input_file")
@click.option("--output", "-o", default="", help="Output script path")
@click.option("--focus", "-f", default="", help="Focus areas")
def script(input_file: str, output: str, focus: str):
    """Generate a dialogue script from a Markdown file (no audio)."""
    from .script import generate_script as gen_script
    from .utils import read_document, slugify

    config, root = load_config()
    paths = config.resolve_paths(root)
    scripts_dir = paths["scripts"]
    scripts_dir.mkdir(parents=True, exist_ok=True)

    content = read_document(input_file)
    if len(content) > 120000:
        content = content[:120000]

    click.echo(f"Generating script from {input_file} ({len(content)} chars)...")
    dialogue = gen_script(content, config, root, focus)

    if output:
        out_path = Path(output)
    else:
        slug = slugify(Path(input_file).stem)
        out_path = scripts_dir / f"{slug}_script.json"

    out_path.write_text(json.dumps(dialogue, ensure_ascii=False, indent=2), encoding="utf-8")
    click.echo(f"Script saved: {out_path} ({len(dialogue)} turns)")


@main.command()
@click.argument("topic", required=False, default="")
@click.option("--description", "-d", default="", help="Topic description for targeted search")
@click.option("--output", "-o", default="", help="Output file path (default: episodes/<slug>.md)")
@click.option("--queries", "-q", multiple=True, help="Custom search queries (repeat for multiple)")
def research(topic: str, description: str, output: str, queries: tuple):
    """Research a topic via web search and generate a Markdown research document."""
    from .research import research_topic
    from .utils import slugify

    config, root = load_config()

    if not config.research.enabled:
        click.echo("  Research is disabled. Set research.enabled: true in podcraft.yaml.")
        return

    if not topic:
        click.echo("  Please provide a topic: podcraft research \"Your Topic\"")
        return

    click.echo(f"Researching: {topic}")
    search_queries = list(queries) if queries else None
    result = research_topic(topic, config, root, search_queries, description)

    if output:
        out_path = Path(output)
    else:
        episodes_dir = root / "episodes"
        episodes_dir.mkdir(exist_ok=True)
        out_path = episodes_dir / f"{slugify(topic)}.md"

    out_path.write_text(result, encoding="utf-8")
    click.echo(f"Research saved: {out_path} ({len(result)} chars)")


@main.command("auto-publish")
@click.option("--queue", "-q", default="publish-queue.json", help="Queue file path")
def auto_publish(queue: str):
    """Publish the next episode from the publish queue."""
    from .queue import get_next, pop_queue
    from .pipeline import publish as run_publish

    config, root = load_config()
    queue_path = root / queue

    item = get_next(queue_path)
    if not item:
        click.echo("  Queue is empty.")
        return

    episode_num = item.get("episode", 0)
    title = item.get("title", "")
    research_file = item.get("research_file", "")

    click.echo(f"Next in queue: {title}")

    # Check research file exists
    if research_file:
        research_path = root / research_file
        if not research_path.exists():
            click.echo(f"  Research file not found: {research_path}")
            click.echo("  Run 'podcraft research' first or check the path in publish-queue.json.")
            return
        input_path = str(research_path)
    else:
        click.echo("  No research_file in queue item. Cannot publish.")
        return

    result = run_publish(input_path, config, root, title, episode_num)

    # Only pop queue if publishing fully succeeded.
    # If release is enabled, require audio_url; otherwise, require audio_file.
    success = bool(result.get("audio_url")) if config.release.enabled else bool(result.get("audio_file"))
    if success:
        popped = pop_queue(queue_path)
        click.echo(f"  Popped from queue: {popped.get('title', '')}")
        from .queue import queue_length
        remaining = queue_length(queue_path)
        click.echo(f"  Queue remaining: {remaining}")
    else:
        click.echo("  Publish incomplete — queue not popped. Fix the issue and retry.")


@main.command()
def feed():
    """Regenerate the RSS feed from existing episodes."""
    from .feed import build_rss
    from datetime import datetime, timezone, timedelta

    config, root = load_config()
    paths = config.resolve_paths(root)
    output_dir = paths["output"]

    episodes = sorted(output_dir.glob("*.mp3"))
    if not episodes:
        click.echo("No episodes found in output directory.")
        return

    tz = timezone(timedelta(hours=8))
    ep_list = []
    for i, ep_path in enumerate(episodes):
        notes_path = paths["scripts"] / f"{ep_path.stem}_notes.txt"
        description = notes_path.read_text(encoding="utf-8") if notes_path.exists() else f"{config.podcast.title} - {ep_path.stem}"

        ep_list.append({
            "title": f"EP{i + 1:02d}: {ep_path.stem.replace('-', ' ').replace('_', ' ').title()}",
            "description": description,
            "audio_file": str(ep_path),
            "audio_url": f"{config.feed.audio_base_url}/{ep_path.name}",
            "pub_date": datetime.now(tz) - timedelta(days=len(episodes) - i - 1),
            "episode_number": i + 1,
        })

    rss_xml = build_rss(ep_list, config)
    feed_path = root / config.feed.output
    feed_path.write_text(rss_xml, encoding="utf-8")
    click.echo(f"RSS feed updated: {len(ep_list)} episodes → {feed_path}")
